package EX_01_java_basic;

public class NamingConvention {
//    Start with a "Capital-letter"
//    for example KrishnaVamshi
public static void main(String[] args) {
    System.out.println("KrishnaVamshi");
//    Never use "space"
//    never start with .
//    never leave empty
//    never start with an integer
}
}
