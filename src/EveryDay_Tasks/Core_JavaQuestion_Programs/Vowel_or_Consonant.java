package EveryDay_Tasks.Core_JavaQuestion_Programs;

public class Vowel_or_Consonant {
    public static void main(String[] args) {
        char ch = 'e';
        if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u')
            System.out.println("Vowel");
        else
            System.out.println("Consonant");
    }
}
